    value: "已接入",
    accent: "blue" as const,
    hint: "支持层级展示"
  },
  {
    label: "权限维护",
    value: "已接入",
    accent: "purple" as const,
    hint: "支持权限标识与路由维护"
  }
]);

function onFullscreen() {
  // 重置表格高度
  tableRef.value.setAdaptive();
}
</script>

<template>
  <AdminModuleShell
    title="菜单权限配置"
    description="承接批发商城管理端菜单树、路由入口与权限接口维护。"
    api-path="/manager/permission/menu"
    :tips="['菜单节点维护', '权限接口绑定', '支持树形结构']"
    :summary-cards="summaryCards"
  >
    <div class="main">
    <el-form
      ref="formRef"
      :inline="true"
      :model="form"
      class="search-form bg-bg_color w-full pl-8 pt-3 overflow-auto"
    >
      <el-form-item label="菜单名称：" prop="title">
        <el-input
          v-model="form.title"
          placeholder="请输入菜单名称"
          clearable
          class="w-45!"
        />
      </el-form-item>
      <el-form-item>
        <el-button
          type="primary"
          :icon="useRenderIcon('ri/search-line')"
          :loading="loading"
          @click="onSearch"
        >
          搜索
        </el-button>
        <el-button :icon="useRenderIcon(Refresh)" @click="resetForm(formRef)">
          重置
        </el-button>
      </el-form-item>
    </el-form>

    <PureTableBar
      title="平台菜单台账"
      :columns="columns"
      :isExpandAll="false"
      :tableRef="tableRef?.getTableRef()"
